fun main(){
    println("Data Type:")

//   Integer type
    val x : Int = 5
    println(x)

//    Float type . value assign er pore f likhtei hobe otehr wise error dibe.Float er bit 32
    val y : Float = 5.34f
    println(y)

//    Double type .Double er bit 64
    val z : Double = 5.34333333
    println(z)

//    Boolean type
    val a : Boolean = true
    println(a)

//    String type
    var name : String = "Kotlin"
    println(name)

//    Character Type -> store only one character
    var char : Char = 'A'
    println(char)
}